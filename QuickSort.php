<?php
/*
 * Быстрая сортировка — это алгоритм сортировки, который основан на принципе "разделяй и властвуй".
 * Он разбивает список элементов на две части,
 * затем рекурсивно сортирует каждую из этих частей и, наконец,
 * объединяет отсортированные части в один отсортированный список.
 */

function quick_sort($arr) {
    $length = count($arr);
    if ($length <= 1) {
        return $arr;
    } else {
        $pivot = $arr[0];
        $left = array();
        $right = array();
        for ($i = 1; $i < $length; $i++) {
            if ($arr[$i] < $pivot) {
                $left[] = $arr[$i];
            } else {
                $right[] = $arr[$i];
            }
        }
        return array_merge(quick_sort($left), array($pivot), quick_sort($right));
    }
}

// Пример использования
$my_array = array(3, 0, 2, 5, -1, 4, 1);
echo "Исходный массив: ";
echo implode(", ", $my_array) . PHP_EOL;

$sorted_array = quick_sort($my_array);
echo "Отсортированный массив: ";
echo implode(", ", $sorted_array);

