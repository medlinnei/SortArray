<?php
/*
 * Сортировка расчёской - это алгоритм сортировки, который является улучшенной версией пузырьковой сортировки.
 * Он работает путем сравнения элементов списка и обмена местами тех,
 * которые находятся в неправильном порядке.
 */

function comb_sort($arr) {
    $gap = count($arr);
    $swap = true;
    $factor = 1.3;

    while ($gap > 1 or $swap) {
        if ($gap > 1) {
            $gap = floor($gap / $factor);
        }

        $swap = false;
        $i = 0;

        while ($i + $gap < count($arr)) {
            if ($arr[$i] > $arr[$i + $gap]) {
                $temp = $arr[$i];
                $arr[$i] = $arr[$i + $gap];
                $arr[$i + $gap] = $temp;
                $swap = true;
            }

            $i++;
        }
    }

    return $arr;
}

// Пример использования
$my_array = array(3, 0, 2, 5, -1, 4, 1);
echo "Исходный массив: ";
echo implode(", ", $my_array) . PHP_EOL;

$sorted_array = comb_sort($my_array);
echo "Отсортированный массив: ";
echo implode(", ", $sorted_array);

