<?php
/*
 * Пузырьковая сортировка - это простой алгоритм сортировки,
 * который проходит по списку несколько раз и каждый раз сравнивает два соседних элемента,
 * меняя их местами, если они стоят в неправильном порядке.
 * Алгоритм продолжает проходить по списку до тех пор,
 * пока все элементы не будут расположены в правильном порядке.
 */
function bubble_sort($arr) {
    $n = count($arr);
    for ($i = 0; $i < $n; $i++) {
        for ($j = 0; $j < $n - $i - 1; $j++) {
            if ($arr[$j] > $arr[$j + 1]) {
                $temp = $arr[$j];
                $arr[$j] = $arr[$j + 1];
                $arr[$j + 1] = $temp;
            }
        }
    }
    return $arr;
}

// Пример использования
$my_array = array(3, 0, 2, 5, -1, 4, 1);
echo "Исходный массив: ";
echo implode(", ", $my_array) . PHP_EOL;

$sorted_array = bubble_sort($my_array);
echo "Отсортированный массив: ";
echo implode(", ", $sorted_array);

/*
 *  В этом примере мы определяем функцию bubble_sort,
 *  которая принимает массив чисел в качестве параметра и возвращает отсортированный массив.
 *  В функции мы используем два вложенных цикла for для прохода по списку и сравнения
 *  каждого элемента со следующим. Если текущий элемент больше следующего, мы меняем их местами.
 *  Когда проходы циклов заканчиваются, мы возвращаем отсортированный массив.
*/